# While Task - Create the couting robot 1-30, start from 1 to 30 with the multiple 4.
number = 1
while number < 31:
    print(number)
    number += 4
else:
    print("The while loop is finished.")

# For Task - Create the couting robot 1-30, start from 1 to 30 with the multiple 4.
for i in range(1, 31, 4):
    print(i)
else:
    print("The for loop is finished.")